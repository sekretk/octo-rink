namespace Framework.UI.Controls
{
    /// <summary>
    /// The display number formats for an elastic group.
    /// </summary>
    public enum ElasticGroupNumberFormat
    {
        /// <summary>
        /// The number of items applicable at any one time.
        /// </summary>
        Value,

        /// <summary>
        /// The total number of items.
        /// </summary>
        Total,

        /// <summary>
        /// The number of items applicable over the total.
        /// </summary>
        ValueAndTotal
    }
}
